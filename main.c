int main(int argc, char **argv)
{
    switch(argc)
    {
        case 2:
            if (!strcmp(argv[1], "-v"))
            {
                bsp_cmd_init();
            }
            else
            {
                printf("bad arguments.\n");
                return EXIT_SUCCESS;
            }

            app_run();
        break;

        case 1:
            app_run();
        break;

        default:
            printf("too few arguments.\n");
            return EXIT_SUCCESS;
    }  
    
    return EXIT_SUCCESS;
}